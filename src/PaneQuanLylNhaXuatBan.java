    private void LoadBang() {
        jTableNhaXuatBan.removeAll();
        String[] tieuDe = {"STT", "Tên nhà xuất bản", "Địa chỉ", "Số điện thoại"};
        DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
        int i = 1;
        for (DTONhaXuatBan nhaXuatBan : nhaXuatBans) {
            Vector vt = new Vector();
            vt.add(String.valueOf(i));
            vt.add(nhaXuatBan.getTenNXB());
            vt.add(nhaXuatBan.getDiaChi());
            vt.add(nhaXuatBan.getSoDT());
            model.addRow(vt);
            i++;
        }
        jTableNhaXuatBan.setModel(model);
    }

    private void LoadData() {
        try {
            nhaXuatBans.clear();
            rs = nhaXuatBan.ShowNhaXuatBan();
            while (rs.next()) {
                DTONhaXuatBan a = new DTONhaXuatBan();
                a.setMaNXB(Integer.parseInt(rs.getString("MaNXB")));
                a.setTenNXB(rs.getString("TenNXB"));
                a.setDiaChi(rs.getString("DiaChi"));
                a.setSoDT(rs.getString("SoDT"));
                nhaXuatBans.add(a);
            }
            LoadBang();
        } catch (SQLException e) {
        } catch (Exception ex) {
            Logger.getLogger(PanelQuanLyTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

