    private void jTextFieldSoDTKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSoDTKeyPressed
        // TODO add your handling code here:
        String soDT = jTextFieldSoDT.getText();
        int length = soDT.length();
        char c = evt.getKeyChar();

        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {//Kiểm tra xem số nhập vào có là từ 0-9
            if (length < 11)//kiểm tra độ dài của khung nhập
            {
                jTextFieldSoDT.setEditable(true);
            } else {
                jTextFieldSoDT.setEditable(false);//Nếu >10 thì k cho nhập
            }
        } else {
            //Cho phép sử dụng phím backspace và delete
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                jTextFieldSoDT.setEditable(true);
            } else {
                jTextFieldSoDT.setEditable(false);
            }
        }
    }//GEN-LAST:event_jTextFieldSoDTKeyPressed

