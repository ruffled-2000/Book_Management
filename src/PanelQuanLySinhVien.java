    private void LoadBang() {
        jTableThongTinSV.removeAll();
        String[] tieuDe = {"Mã sinh viên", "Tên sinh viên", "Giới tính", "Địa chỉ", "Email"};
        DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
        int i = 0;
        for (DTOSinhVien a : sinhViens) {
            Vector vt = new Vector();
            vt.add(a.getMaSV());
            vt.add(a.getTenSV());
            if (a.getGioiTinh() == 0)//Nam
            {
                vt.add("Nam");
            } else {
                vt.add("Nữ");
            }
            vt.add(a.getDiaChi());
            vt.add(a.getEmail());
            model.addRow(vt);
            i++;
        }
        jTableThongTinSV.setModel(model);
    }

    private void LoadData() {
        try {
            sinhViens.clear();
            rs = sinhvienBLL.ShowSinhVien();
            while (rs.next()) {
                DTOSinhVien a = new DTOSinhVien();
                a.setMaSV(rs.getString("MaSV"));
                a.setTenSV(rs.getString("TenSV"));
                a.setGioiTinh(Integer.parseInt(rs.getString("GioiTinh")));
                a.setDiaChi(rs.getString("DiaChi"));
                a.setEmail(rs.getString("Email"));
                sinhViens.add(a);
            }
            LoadBang();
        } catch (SQLException e) {
        } catch (Exception ex) {
            Logger.getLogger(PanelQuanLyTaiKhoan.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

