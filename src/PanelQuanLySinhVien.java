    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
        String regexEmail="^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
        + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";;
        Pattern pattern = Pattern.compile(regexEmail);
        try {
            if (jTextFieldMaSV.getText().equals("") || jTextFieldTenSV.getText().equals("") || jTextFieldDiaChi.getText().equals("") || jTextFieldEmail.getText().equals("")) {
                throw new Exception("Không được bỏ trống các ô nhập liệu");
            }
            ResultSet rs = sinhvienBLL.getThongTinSinhVien(jTextFieldMaSV.getText());
            if(rs.next()==true)
                throw new Exception("Đã có sinh viên này");
            Matcher matcher = pattern.matcher(jTextFieldEmail.getText());
            if(matcher.find()==false)
                throw new Exception("Email không đúng định dạng");
            String maSV = jTextFieldMaSV.getText();
            String tenSV = jTextFieldTenSV.getText();
            int gioiTinh;
            if (jRadioButtonNam.isSelected()) {
                gioiTinh = 0;
            } else {
                gioiTinh = 1;
            }
            String diaChi = jTextFieldDiaChi.getText();
            String email = jTextFieldEmail.getText();
            sinhvienBLL.insertSinhVien(maSV, tenSV, gioiTinh, diaChi, email);
            LoadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jButtonTimKiem, e.getMessage(), "Lỗi nhập liệu", HEIGHT);
        }

    }//GEN-LAST:event_jButtonThemActionPerformed

    private void jButtonXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonXoaActionPerformed
        // TODO add your handling code here:

        try {
            int index = jTableThongTinSV.getSelectedRow();
            if (index < 0) {
                throw new Exception("Vui lòng chọn thành viên cần xóa");
            }
            int rsPane = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (rsPane == JOptionPane.YES_OPTION) {
                sinhvienBLL.deleteSinhVien(sinhViens.get(index).getMaSV());
                sinhViens.remove(index);
                LoadBang();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jButtonXoa, e.getMessage(), "Lỗi", HEIGHT);
        }
    }//GEN-LAST:event_jButtonXoaActionPerformed

    private void jButtonSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSuaActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextFieldMaSV.getText().equals("") || jTextFieldTenSV.getText().equals("") || jTextFieldDiaChi.getText().equals("") || jTextFieldEmail.getText().equals("")) {
                throw new Exception("Không được bỏ trống các ô nhập liệu");
            }
            String maSV = jTextFieldMaSV.getText();
            String tenSV = jTextFieldTenSV.getText();
            int gioiTinh;
            if (jRadioButtonNam.isSelected()) {
                gioiTinh = 0;
            } else {
                gioiTinh = 1;
            }
            String diaChi = jTextFieldDiaChi.getText();
            String email = jTextFieldEmail.getText();
            int indexTable = jTableThongTinSV.getSelectedRow();
            sinhViens.set(indexTable, new DTOSinhVien(maSV, tenSV, gioiTinh, diaChi, email));
            sinhvienBLL.updateSinhVien(maSV, tenSV, gioiTinh, diaChi, email);
            LoadBang();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jButtonTimKiem, e.getMessage(), "Lỗi nhập liệu", HEIGHT);
        }
    }//GEN-LAST:event_jButtonSuaActionPerformed

